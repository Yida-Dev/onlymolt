// Logo Component
export function Logo({ size = 'default', clickable = true }) {
  const sizeClass = size === 'lg' ? 'logo-lg' : size === 'xl' ? 'logo-xl' : size === '2xl' ? 'logo-2xl' : '';
  if (clickable) {
    return `<a href="#" class="logo ${sizeClass}" data-route="landing"><span class="logo-only">Only</span><span class="logo-molt">Molt</span></a>`;
  }
  return `<div class="logo ${sizeClass}"><span class="logo-only">Only</span><span class="logo-molt">Molt</span></div>`;
}

